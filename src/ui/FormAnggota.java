    private void loadTable() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nama");

        ArrayList<Anggota> list = dao.getAll();

        for (Anggota a : list) {
            model.addRow(new Object[]{
                a.getId(),
                a.getNama()
            });
        }

        tblAnggota.setModel(model);
    }

